#include <stdint.h>
#include <stdio.h>






int
fn (int indexes[9], int i, float *board)
{
  int handicap = 0;
  int move_num = 0;
  for (i = 0; i < handicap; ++i)
    {
      board[indexes[i]] = 1;
      ++move_num;
    }
}
